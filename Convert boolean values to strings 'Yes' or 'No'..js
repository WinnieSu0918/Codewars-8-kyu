/*
  Title:
    Convert boolean values to strings 'Yes' or 'No'
    
  Description:
    Complete the solution so that it reverses the string passed into it.
    
  Examples:
    'world'  =>  'dlrow'
    'word'   =>  'drow'
    
*/
    
// Now Solution
function boolToWord( bool ){
    return bool ? 'Yes' : 'No'
}